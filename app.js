 const hidden_text=document.getElementById("hidden_text");
const read_more=document.getElementById("read_more");
read_more.onclick=()=>{
if(hidden_text.style.display==='none'){
    hidden_text.style.display='inline';
    read_more.textContent='Read less'
}
else{
    hidden_text.style.display='none';
    read_more.textContent ='Read More' ;
}};

